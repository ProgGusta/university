public class UnidadeDePesoUtils {
    // Gramas para Quilogramas
    public static double gramasParaQuilogramas(double gramas) {
        return gramas / 1000.0;
    }

    // Gramas para Libras
    public static double gramasParaLibras(double gramas) {
        return gramas / 453.59237;
    }

    // Gramas para Onças
    public static double gramasParaOunces(double gramas) {
        return gramas / 28.34952;
    }

    // Quilogramas para Gramas
    public static double quilogramasParaGramas(double quilogramas) {
        return quilogramas * 1000.0;
    }

    // Quilogramas para Libras
    public static double quilogramasParaLibras(double quilogramas) {
        return quilogramas * 2.20462;
    }

    // Quilogramas para Onças
    public static double quilogramasParaOunces(double quilogramas) {
        return quilogramas * 35.27396;
    }

    // Libras para Gramas
    public static double librasParaGramas(double libras) {
        return libras * 453.59237;
    }

    // Libras para Quilogramas
    public static double librasParaQuilogramas(double libras) {
        return libras / 2.20462;
    }

    // Libras para Onças
    public static double librasParaOunces(double libras) {
        return libras * 16.0;
    }

    // Onças para Gramas
    public static double ouncesParaGramas(double oncas) {
        return oncas * 28.34952;
    }

    // Onças para Quilogramas
    public static double ouncesParaQuilogramas(double oncas) {
        return oncas / 35.27396;
    }

    // Onças para Libras
    public static double ouncesParaLibras(double oncas) {
        return oncas / 16.0;
    }
}